 				<li>Commencez par marcher sur place et mettez-vous à l’écoute de vos sensations</li>
 				<li>Passez de la marche statique à la course statique</li>
 				<li>Quand vous vous sentez suffisamment confiant, laissez-vous tomber vers l’avant !</li>
